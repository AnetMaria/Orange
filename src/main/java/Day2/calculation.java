package Day2;

public class calculation {

    int sum(int num1,int num2) {
        int s = num1 + num2;// local variables num1 and num2
        return s;
    }
    int sub(int num1, int num2){
        int b = num1 - num2;// local variables num1 and num2
        return b;
    }
    int mul(int num1,int num2){
        return num1*num2;// local variables num1 and num2
    }
    int div(int num1,int num2){
        return num1/num2;// local variables num1 and num2
    }

}
